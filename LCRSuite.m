% --- Executes on button press in pushbuttonConnect.
function pushbuttonConnect_Callback(hObject, eventdata, handles)
% hObject    handle to pushbuttonConnect (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
read_boxes;
set(handles.pushbuttonConnect, 'Enable', 'off');
drawnow();
switch connected
    case 'Not Connected'
        status = openE4980();
        if (status == 1)
            set(handles.text12, 'String', 'Connected');
            set(handles.text12, 'ForegroundColor', 'green');
            set(handles.pushbuttonConnect, 'String', 'Disconnect LCR');
        end
    case 'Connected'
        status = closeE4980();
        if (status == 0)
            set(handles.text12, 'String', 'Not Connected');
            set(handles.text12, 'ForegroundColor', 'red');
            set(handles.pushbuttonConnect, 'String', 'Connect to LCR');
        end
end
set(handles.pushbuttonConnect, 'Enable', 'on');
                

