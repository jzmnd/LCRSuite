% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
read_boxes;
status = 1;
set(handles.pushbutton1, 'Enable', 'off');
drawnow();
if (strcmp(dcbias, 'None') == true)
    warning('Set DC bias');
    status = 0;
end
if (strcmp(integ, 'None') == true)
    warning('Set integration');
    status = 0;
end
if (strcmp(mmode, 'None') == true)
    warning('Set mode');
    status = 0;
end
if (startFreq == 0)
    warning('Set start frequency');
    status = 0;
end
if (stopFreq == 0)
    warning('Set stop frequency');
    status = 0;
end
if status == 0
    set(handles.pushbutton1, 'Enable', 'on');
    return
end
status = setupLCR(vac, integ, average, dcbias, mmode);
if status == 0
    set(handles.pushbutton1, 'Enable', 'on');
    return
end
[handles.dataf, handles.dataA, handles.dataB] = listsweepFREQ(startFreq, stopFreq);
plot(handles.axes1, handles.dataf, handles.dataA);
plot(handles.axes2, handles.dataf, handles.dataB);
set(handles.axes1, 'XScale', 'log');
set(handles.axes2, 'XScale', 'log');

set(handles.pushbutton1, 'Enable', 'on');

% Update handles structure
guidata(hObject, handles);


